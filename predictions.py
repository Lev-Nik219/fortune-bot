def generate_prediction(user_data):
    """
    Генерация уникального оптимистичного предсказания
    Комбинирует: HuggingFace API -> Заготовленные шаблоны -> Динамические шаблоны
    """
    user_name = user_data.get('name', 'друг')
    user_gender = user_data.get('gender', 'other')
    user_question = user_data.get('question', '')
    
    # 1. Пробуем Hugging Face (уникальная генерация)
    if HF_AVAILABLE:
        try:
            prediction = hf.generate_prediction(user_data)
            if prediction and len(prediction) > 20:
                logger.info("Using Hugging Face generated prediction")
                return f"✨ {prediction}"
        except Exception as e:
            logger.error(f"Hugging Face error: {e}")
    
    # 2. Используем заготовленные шаблоны с рандомизацией
    # Определяем категорию вопроса
    category = 'general'
    if user_question:
        question_lower = user_question.lower()
        if any(word in question_lower for word in ['работ', 'карьер', 'бизнес', 'денег', 'финанс']):
            category = 'career'
        elif any(word in question_lower for word in ['любов', 'отношени', 'сердц', 'встреч']):
            category = 'love'
        elif any(word in question_lower for word in ['здоров', 'энерг', 'спорт']):
            category = 'health'
    
    # Выбираем случайный шаблон из категории
    templates = PREDICTION_TEMPLATES.get(category, PREDICTION_TEMPLATES['general'])
    template = random.choice(templates)
    
    # Определяем обращение в зависимости от пола
    if user_gender == 'female':
        suffix = 'а'
    else:
        suffix = ''
    
    # Создаем уникальное предсказание, комбинируя имя и шаблон
    personalized_templates = [
        f"✨ *{user_name}*, {template}",
        f"🌟 *{user_name}*, я вижу: {template}",
        f"🔮 *{user_name}*, слушай: {template}",
        f"💫 {user_name} дорогой{'' if user_gender != 'female' else 'ая'}, {template}",
        f"✨ Знаешь, {user_name}, {template}"
    ]
    
    prediction = random.choice(personalized_templates)
    
    # Добавляем уникальное окончание
    endings = [
        " Помни: ты уникален(льна) и достоин(на) самого лучшего!",
        " Верь в себя, и все получится!",
        " Звезды с тобой сегодня!",
        " Не упусти свой шанс!",
        " Удачи тебе на этом пути!"
    ]
    prediction += random.choice(endings)
    
    return prediction

def generate_horoscope(zodiac_sign):
    """
    Генерация уникального гороскопа
    Комбинирует: HuggingFace API -> Заготовленные шаблоны -> Динамические варианты
    """
    zodiac_name = zodiac_sign.capitalize()
    
    # 1. Пробуем Hugging Face для уникального гороскопа
    if HF_AVAILABLE:
        try:
            horoscope = hf.generate_horoscope(zodiac_sign, zodiac_name)
            if horoscope and len(horoscope) > 20:
                logger.info(f"Using Hugging Face generated horoscope for {zodiac_sign}")
                return horoscope
        except Exception as e:
            logger.error(f"Hugging Face horoscope error: {e}")
    
    # 2. Используем заготовленные шаблоны с рандомизацией
    zodiac_lower = zodiac_sign.lower()
    templates = HOROSCOPE_TEMPLATES.get(zodiac_lower, HOROSCOPE_TEMPLATES['овен'])
    
    # Выбираем случайный шаблон
    base_horoscope = random.choice(templates)
    
    # Добавляем случайную концовку для разнообразия
    endings = [
        f" {random.choice(['✨', '🌟', '💫', '⭐️', '⚡️'])}",
        " В общем, день будет интересным!",
        " Не упусти свой момент!",
        " Звезды тебе благоволят!",
        " Удачного дня!"
    ]
    
    # Добавляем эмодзи в начало, если его нет
    if not any(emoji in base_horoscope for emoji in ['🔥', '💪', '🍰', '💰', '🗣', '🏠', '🦁', '📋', '⚖️', '🦂', '🎯', '📈', '💡', '🎨']):
        emojis = ['✨', '🌟', '💫', '⭐️', '⚡️']
        base_horoscope = f"{random.choice(emojis)} {base_horoscope}"
    
    return base_horoscope + random.choice(endings)

