def generate_prediction(user_data):
    """Генерация предсказания"""
    user_name = user_data.get('name', 'друг')
    user_gender = user_data.get('gender', 'other')
    user_question = user_data.get('question', '')
    
    # Пробуем Hugging Face
    if HF_AVAILABLE:
        try:
            prediction = hf.generate_prediction(user_data)
            if prediction:
                return f"✨ {prediction}"
        except Exception as e:
            logger.error(f"Hugging Face error: {e}")
    
    # Определяем категорию
    category = 'general'
    if user_question:
        q = user_question.lower()
        if any(w in q for w in ['работ', 'карьер', 'бизнес', 'денег']):
            category = 'career'
        elif any(w in q for w in ['любов', 'отношени', 'сердц']):
            category = 'love'
        elif any(w in q for w in ['здоров', 'энерг']):
            category = 'health'
    
    template = random.choice(PREDICTION_TEMPLATES[category])
    
    endings = [
        " Помни: ты уникален(льна) и достоин(на) самого лучшего!",
        " Верь в себя, и все получится!",
        " Звезды с тобой сегодня!",
        " Не упусти свой шанс!"
    ]
    
    return f"✨ *{user_name}*, {template}{random.choice(endings)}"

