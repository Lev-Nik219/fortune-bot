def generate_prediction(user_data):
    """Генерация уникального предсказания"""
    user_name = user_data.get('name', 'друг')
    user_gender = user_data.get('gender', 'other')
    user_question = user_data.get('question', '')
    
    # Пробуем Hugging Face (если доступен)
    if HF_AVAILABLE:
        try:
            prediction = hf.generate_prediction(user_data)
            if prediction and len(prediction) > 20:
                logger.info("Using Hugging Face prediction")
                return f"✨ {prediction}"
        except Exception as e:
            logger.error(f"Hugging Face error: {e}")
    
    # Определяем категорию вопроса
    category = 'general'
    if user_question:
        q = user_question.lower()
        if any(w in q for w in ['работ', 'карьер', 'бизнес', 'денег', 'финанс']):
            category = 'career'
        elif any(w in q for w in ['любов', 'отношени', 'сердц', 'встреч']):
            category = 'love'
        elif any(w in q for w in ['здоров', 'энерг', 'спорт', 'сон']):
            category = 'health'
    
    # Выбираем шаблон
    template = random.choice(PREDICTION_TEMPLATES[category])
    
    # Разные варианты обращения
    greetings = [
        f"✨ *{user_name}*, ",
        f"🌟 *{user_name}*, я вижу: ",
        f"🔮 *{user_name}*, слушай: ",
        f"💫 Дорогой(ая) *{user_name}*, "
    ]
    
    endings = [
        " Помни: ты уникален(льна) и достоин(на) самого лучшего!",
        " Верь в себя, и все получится!",
        " Звезды с тобой сегодня!",
        " Не упусти свой шанс!",
        " Удачи тебе на этом пути!",
        " ✨"
    ]
    
    prediction = random.choice(greetings) + template + random.choice(endings)
    return prediction

def generate_horoscope(zodiac_sign):
    """Генерация уникального гороскопа"""
    zodiac_lower = zodiac_sign.lower()
    
    # Пробуем Hugging Face
    if HF_AVAILABLE:
        try:
            horoscope = hf.generate_horoscope(zodiac_sign)
            if horoscope and len(horoscope) > 15:
                logger.info("Using Hugging Face horoscope")
                # Добавляем эмодзи если нужно
                zodiac_emoji = {
                    'овен': '🔥', 'телец': '🍰', 'близнецы': '🗣', 'рак': '🏠',
                    'лев': '🦁', 'дева': '📋', 'весы': '⚖️', 'скорпион': '🦂',
                    'стрелец': '🎯', 'козерог': '📈', 'водолей': '💡', 'рыбы': '🎨'
                }.get(zodiac_lower, '✨')
                return f"{zodiac_emoji} {horoscope}"
        except Exception as e:
            logger.error(f"Hugging Face horoscope error: {e}")
    
    # Используем шаблоны
    templates = HOROSCOPE_TEMPLATES.get(zodiac_lower, HOROSCOPE_TEMPLATES['овен'])
    horoscope = random.choice(templates)
    
    # Добавляем случайное завершение
    endings = [" ✨", " 🌟", " 💫", " ⭐️"]
    
    return horoscope + random.choice(endings)